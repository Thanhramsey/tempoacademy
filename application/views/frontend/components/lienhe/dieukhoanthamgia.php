<p>Chúng tôi chịu trách nhiệm về nội dung thông tin cung cấp trên trang thông tin điện tử Ocop Chưpưh , thực hiện quy định của Pháp Luật về giao kết hợp đồng, đặt hàng, thanh toán.</p>

<p>7. Thay đổi, hủy bỏ giao dịch :</p>

<p>Trong mọi trường hợp, khách hàng đều có quyền chấm dứt giao dịch nếu đã thực hiện các biện pháp sau đây:</p>

<p>a. Thông báo cho&nbsp;Ocop Chưpưh&nbsp;về việc hủy giao dịch qua đường dây nóng 02.373.961.818.</p>

<p>b. Trả lại hàng hoá đã nhận nhưng chưa sử dụng hoặc hưởng bất kỳ lợi ích nào từ hàng hóa đó =&gt; theo Điều 4</p>

<p>Người mua chịu trách nhiệm khi có những thiếu sót, nhầm lần trong việc thanh toán tiền cho Nhà cung cấp.</p>

